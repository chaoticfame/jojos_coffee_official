          <p>JoJo's Bizarre Adventure (ジョジョの奇妙な冒険, JoJo no Kimyō na Bōken) is a Japanese manga series written and illustrated by Hirohiko Araki. It was originally serialized in Shueisha's shōnen manga magazine Weekly Shōnen Jump from 1987 to 2004, and later in Ultra Jump from 2005 onward.</p>
          <p>The series is divided into nine story arcs, each following a new protagonist bearing the "JoJo" nickname. The first six arcs take place within a single continuity, while parts 7 and 8 take place in an alternate universe. The ninth part, The JoJoLands, began serialization in 2023.</p>
          <p>Each part follows the adventures of a member of the Joestar family, who possesses a star-shaped birthmark above their left shoulder blade. The various parts feature different genres, including action-adventure, mystery, suspense, and slice of life, with a notable fantasy element running through them.</p>
          <p>JoJo's Bizarre Adventure is known for its distinctive art style, dramatic poses, and creative use of supernatural powers. The series has become a cultural phenomenon, inspiring countless references, memes, and a dedicated fanbase worldwide. The anime adaptation has further popularized the series, bringing Araki's unique vision to a global audience.</p>
          <p>At JoJo's Bizarre Café, we celebrate this legendary series by crafting beverages inspired by the iconic characters, stands, and moments that have made JoJo's Bizarre Adventure a timeless masterpiece in the world of manga and anime.</p>
